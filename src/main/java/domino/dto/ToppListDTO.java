package domino.dto;

public class ToppListDTO {
	private String topp_name;
	private int topp_price;
	private String topp_img;
	private String topp_type;
	
	public String getTopp_name() {
		return topp_name;
	}
	public void setTopp_name(String topp_name) {
		this.topp_name = topp_name;
	}
	public int getTopp_price() {
		return topp_price;
	}
	public void setTopp_price(int topp_price) {
		this.topp_price = topp_price;
	}
	public String getTopp_img() {
		return topp_img;
	}
	public void setTopp_img(String topp_img) {
		this.topp_img = topp_img;
	}
	public String getTopp_type() {
		return topp_type;
	}
	public void setTopp_type(String topp_type) {
		this.topp_type = topp_type;
	}
}
