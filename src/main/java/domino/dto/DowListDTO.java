package domino.dto;

public class DowListDTO {
	private String dow_name;
	private int dow_price;
	private String dow_img;
	
	public String getDow_name() {
		return dow_name;
	}
	public void setDow_name(String dow_name) {
		this.dow_name = dow_name;
	}
	public int getDow_price() {
		return dow_price;
	}
	public void setDow_price(int dow_price) {
		this.dow_price = dow_price;
	}
	public String getDow_img() {
		return dow_img;
	}
	public void setDow_img(String dow_img) {
		this.dow_img = dow_img;
	}
	
	
}
