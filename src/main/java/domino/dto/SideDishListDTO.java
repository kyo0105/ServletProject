package domino.dto;

public class SideDishListDTO {

	private String side_name;
	private int side_price;
	private String side_type;
	private String side_img;
	
	public String getSide_name() {
		return side_name;
	}
	public void setSide_name(String side_name) {
		this.side_name = side_name;
	}
	public int getSide_price() {
		return side_price;
	}
	public void setSide_price(int side_price) {
		this.side_price = side_price;
	}
	public String getSide_type() {
		return side_type;
	}
	public void setSide_type(String side_type) {
		this.side_type = side_type;
	}
	public String getSide_img() {
		return side_img;
	}
	public void setSide_img(String side_img) {
		this.side_img = side_img;
	}
	
}
